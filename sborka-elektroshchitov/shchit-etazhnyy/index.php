<h2>Устройство этажных электрических щитов</h2>
<p>
	 Сварной корпус электрического этажного щита выполнен из металлических листов, покрытых эпоксидно-полиуретановой краской светло-серого оттенка. Эта защитная пленка повышает механическую прочность оборудования и защищает сталь от появления коррозии.
</p>
<p>
	 Стандартные модели разделены на 3 отдельные функциональные секции:
</p>
<ol>
	<li>Вводно-учетный – в этом отсеке установлено вводное устройство, клеммники, шины, аппарат отключения магистральной линии и электрические счетчики, количество которых равняется числу квартир. В запирающейся на ключ дверце этажного щита размещены специальные ревизионные окошки из ударопрочного прозрачного поликарбоната, позволяющие снимать показания счетчиков и тем самым контролировать потребление электроэнергии.</li>
	<li>Абонентский или распределительный – предназначен для разводки проводов по отдельным офисам или квартирам и установки, а также для монтажа УЗО и автоматических выключателей, предназначенных для защиты питающих линий. Сам отсек запирается на защелку.</li>
	<li>Слаботочный отсек – в нем можно увидеть линии охранно-пожарной сигнализации, а также телевизионные, телефонные, радио- и интернет-кабели. Во избежание несанкционированного доступа к токонесущим элементам и возможной порчи этот отдел закрывают на ключ.</li>
</ol>
<h2>Общие требования к этажным щитам</h2>
<p>
	 При установке этажных щитов необходимо соблюдать следующие условия:
</p>
<ul>
	<li>Температура в месте подключения ЩЭ – от +10 до +45°C;</li>
	<li>Влажность воздуха – не более 80% при температурном режиме +25°C;</li>
	<li>Наличие схемы на внутренней стороне дверцы&nbsp;этажного щита;</li>
	<li>Наличие на корпусе предупреждающих надписей;</li>
	<li>Угол открывания дверец&nbsp;этажного щита – не меньше 95°;</li>
	<li>Обязательная маркировка всех размещенных внутри проводов;</li>
	<li>Быстрый доступ к щитку мастеров, отвечающих за обслуживание и проведение ремонтных работ;</li>
	<li>Тип помещения – закрытый, оснащенный системой искусственной вентиляции (УХЛ-4 по классификации ГОСТ 15150).</li>
</ul>
<p>
	 Также следует отметить, что применение этажного щита возможно только в том случае, если в помещении нет токопроводящей пыли, а само оно удалено от источников тепла, канализации и водопровода. Еще одно важное условие – отсутствие повышенной влажности.
</p>
<p>
	 Сборка щитка и размещение необходимых устройств выполняется заранее. На объекте проводится только крепление оборудования к стене и подключение всех кабелей. Удобство монтажа этажных щитов обеспечивают крепежи. Для проведения проводов вводных и выводных линий в корпусе ящика предусмотрены специальные отверстия. Дальнейшая установка электрических этажных щитов проходит в определенной последовательности.
</p>
<p>
	 Этап 1. Монтаж корпуса – навеска выполняется через петли или отверстия, позволяющие прикрепить щиток к стене при помощи дюбелей.
</p>
<p>
	 Этап 2. Выравнивание проводов, разделение их на группы, развод в стороны и обрезание нужной длины.
</p>
<p>
	 Этап 3. Подключение нагрузки, а также проводов нуля и заземления.
</p>
<p>
	 Этап 4. Подсоединение выводных линий, необходимых для проверки правильности установки.
</p>
<p>
	 Этап 5. Тестовый запуск оборудования и проверка его работоспособности.
</p>
<p>
	 На этом монтаж этажного щита считается завершенным, а само устройство может быть сдано в эксплуатацию. Все указанные требования необходимо тщательно соблюдать – это предупреждает риск возможного возгорания оборудования и обеспечивает безопасность окружающих.
</p>
