<h2>Конструкция квартирного щита</h2>
<p>
	 Квартирные распределительные щиты представляют собой ящик, корпус которого выполнен из качественного прочного пластика, а внутреннее пространство заполнено специальными аппаратами и приборами распределения и учета электрического тока для установки в жилых помещениях. Система включает в себя вводные аппараты – пакетные выключатели-автоматы, шины N и PE, контактные зажимы, клеммник, счетчик, нулевые проводники, поляризованное реле, фазовые провода, УЗО и пр. Комплектующие собираются в индивидуальном порядке с учетом предоставленной заказчиком схемы. Это напрямую влияет на стоимость готовой конструкции.
