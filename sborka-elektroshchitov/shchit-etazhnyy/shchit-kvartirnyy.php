<h2>Монтаж и особенности подключения квартирного электрического щитка</h2>
<p>
	 Устанавливать слаботочный электрический квартирный ящик должен квалифицированный мастер с большим опытом работы и соответствующей категорией допуска. Выполнять эту процедуру самостоятельно категорически нельзя.
</p>
<p>
	 Перед началом монтажа специалист предварительно делает расчеты и проводит планирование с учетом технических характеристик оборудования и объема энергопотребления. Это позволяет рассчитать нагрузку и объединить приборы в группы с максимальной точностью. Тип разводки зависит от того, в каком порядке расположены подключаемые к щиту электроприборы. Не меньшую роль играет и количество проводов, выводимых из устройства. Выполнив всю подготовку, мастер выбирает оптимальное место для установки квартирного щита. В большинстве случаев это коридор либо прихожая – из этих комнат не придется прокладывать провода слишком далеко.
</p>
<p>
	 Само оборудование можно прикрепить к стене на шурупы или встроить в нишу. Питающие кабели подключаются через отверстия, расположенные в верхней и нижней части квартирного щита. Токоведущие части устройства находятся в прочном шкафу и надежно защищены от механических воздействий, попадания воды и несанкционированного доступа посторонних.
</p>
