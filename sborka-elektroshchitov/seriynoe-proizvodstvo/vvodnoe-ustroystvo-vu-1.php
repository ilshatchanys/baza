<h2>Правила эксплуатации устройства ВУ-1 лифта</h2>
<p>
	 Для правильной работы вводного устройства лифта с конденсаторами необходимо придерживаться определенных правил эксплуатации:
</p>
<ul>
	<li>Окружающая среда – безопасная, без содержания агрессивных компонентов, способных привести к разрушению изоляции проводов и металлического корпуса;</li>
	<li>Отсутствие прямых осадков и солнечных лучей;</li>
	<li>Допустимый диапазон рабочих температур – от -25 до +60°C;</li>
	<li>Высота монтажа над уровнем моря – не больше 4300 м;</li>
	<li>Влажность воздуха – не выше 80% при температуре плюс 25°С и отсутствии конденсата.</li>
</ul>
<p>
	 Само вводное устройство выпускается в климатическом исполнении УХЛ3 и УХЛ 4 (ГОСТ 15150 по запросу УТ 3, УТ 4, О 3, О 4). Установка ВУ-1 должна проводиться профессиональными мастерами в соответствии с паспортом, ТУ, ПУЭ и СКЗ на устройства ввода и инструкцией производителя. ВУ-1 монтируют на твердую ровную поверхность. Допустимое вертикальное отклонение с креплением к стене составляет не больше 50 градусов. По окончанию установки рабочие удаляют загрязнения и проверяют контактные соединения. Если на улице холодно, перед процессом установки вводное устройство с конденсаторами выдерживают при температуре около 15°C. Это позволяет избежать образования конденсата.
</p>
<p>
