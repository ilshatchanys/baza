<h2>Принцип работы ШАВР</h2>
<p>
	 В основе работы щита автоматического ввода лежит непрерывное отслеживание уровня напряжения на основном вводе с помощью реле контроля. Как только он упадет, переключатель переведет электропитание на резервный ввод. В зависимости от используемой базы элементов скорость срабатывания блока АВР составляет от 0,3 до 0,8 секунд.
</p>
<p>
	 При этом в случае возникновении аварийной ситуации на основном вводе щит проверит отсутствие КЗ на резервном и только после этого выполнит переключение. Если АВР идет с функцией восстановления, после возобновления питания основной ввод продолжит свою работу.
</p>
<p>
	 В зависимости от технических особенностей и экономической целесообразности АВР может выполнять отключение с главного ввода на резерв как целого объекта (здания или этажа), так и самых критических его частей (систем безопасности, серверных, оборудования автоматизации производственных процессов и пр.)
</p>
