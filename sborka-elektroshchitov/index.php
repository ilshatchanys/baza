<h2>Назначение электрощитового оборудования</h2>
<p>
	 Сборка электрощита&nbsp;выполняется в следующих целях:
</p>
<ul>
	<li>Создание систем безопасного применения систем освещения и электрических приборов;</li>
	<li>Переключение на систему резервного электропитания – обеспечивает надежную&nbsp;защиту&nbsp;сети от&nbsp;перегрузок;</li>
	<li>Распределение&nbsp;электроэнергии к конечным потребителям;</li>
	<li>Невозможность провести несанкционированный доступ к&nbsp;оборудованию,&nbsp;пребывающему под высоким напряжением.</li>
</ul>
<h2>Виды электрощитового оборудования</h2>
<p>
	 Электрощиты&nbsp;бывают нескольких разновидностей. Именно от этого зависят особенности их&nbsp;сборки:
</p>
<ol>
	<li>Главные&nbsp;щиты&nbsp;– служат для приема и&nbsp;распределения электроснабжения,&nbsp;а также&nbsp;управления&nbsp;и технического мониторинга&nbsp;оборудования.</li>
	<li>Вводно-распределительные электрощиты – принимают, учитывают и распределяют электроэнергию. При установке в шкафах ВРУ используются для защиты от коротких замыканий и перегрузок в сетях электроснабжения.</li>
	<li>Распределительные щиты – устанавливаются на линиях электроснабжения после ВРУ или ГРЩ, управляют работой групповых линий, распределяют электроэнергию.</li>
