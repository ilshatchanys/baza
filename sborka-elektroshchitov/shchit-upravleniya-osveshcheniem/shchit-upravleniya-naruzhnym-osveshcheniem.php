<h2>Правила установки ЩО</h2>
<p>
	 Установка щитов управления уличным освещением начинается с выбора подходящего места. Это должно быть сухое помещение, не размещенное под ванной или душевой. Дальнейшая сборка и монтаж выполняются по такой схеме:
</p>
<ol>
	<li>Шкаф освещения должен быть расположен на расстоянии 1 м от труб коммуникации.</li>
	<li>Встраиваемое оборудование занимает меньше места.</li>
	<li>Для установки внутри помещения подойдет изделие из пластика, для улицы – из металла. В последнем случае нужно обязательно подключить заземление.</li>
	<li>В доме установка щитка может проводиться на высоте от 20 см от поверхности пола. На улице этот показатель должен составлять не меньше 1 м.</li>
	<li>Планируя вмонтировать в шкаф измерительные устройства, обязательно обеспечьте его обогрев.</li>
	<li>При выборе размера щита учитывайте то количество оборудования, которое будет в нем установлено.</li>
</ol>
