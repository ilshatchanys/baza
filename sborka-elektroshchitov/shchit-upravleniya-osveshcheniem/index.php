	<li>Обеспечение строительного и ландшафтного освещения, сигнализации, энергосберегающих сетей и связи, светофоров;</li>
	<li>Управление уличными системами освещения на трассах и в населенных пунктах;</li>
	<li>Защита осветительного оборудования от перегрузок и скачков напряжения.</li>
</ul>
<p>
	 Помимо этого шкафы ШУО исполняют функцию счетчиков электроэнергии и приборов для измерения параметров электрического тока. Также они оповещают оператора об аварийной ситуации и запускают дополнительный режим работы.
</p>
