</p>
<p>
	 По методу монтажа:
</p>
<ul>
	<li>Встраиваемые – нуждаются в подготовке специального углубления, предназначенного для монтажа корпуса устройства;</li>
	<li>Накладные – крепятся на стену или любую другую вертикальную поверхность, корпус находится снаружи.</li>
</ul>
<p>
