<h2>Функции, которые выполняет шкаф учета электроэнергии</h2>
<p>
	 Шкаф учета, представленный в интернет-каталоге нашего магазина, служит для:
</p>
<ol>
	<li>Учета расхода электричества.</li>
	<li>Распределение всего количества переменного тока на ту или иную сеть (одно- или трехфазную).</li>
	<li>Защита электросети от аварийных ситуаций, связанных с нарушением работы системы (замыканий, перегрузок, пробоев цепей на выходе и др.).</li>
</ol>
<p>
	 Каждая из этих функций заложена разработчиками для выполнения правил пожарной безопасности, ведь именно неправильное подключение и настройка проводки или оборудования становятся главной причиной возгорания. Кроме того при установке щита учета электроэнергии обязательно монтируются автоматы на вводе и ампераж трансформаторов тока.
</p>
<h2>Сфера применения</h2>
<p>
	 Шкафы учета электроэнергии применяют для передачи показаний в загородных поселениях и коттеджах. Прибор устанавливают на фасады зданий или опоры электропередач – это облегчает процесс снятия показаний со счетчика уполномоченным лицом. Для качественной защиты изделия от внешних воздействий рекомендуется отдавать предпочтение щитам учета электроэнергии с антивандальным морозостойким корпусом. Кроме того распределительные шкафы могут устанавливаться и применяться на таких объектах, как:
</p>
<ul>
	<li>Торговые площади;</li>
	<li>Производственные цеха с небольшой площадью;</li>
	<li>Офисные помещения;</li>
	<li>Заведения общепита.</li>
</ul>
<h2>Из каких деталей состоит шкаф учета электроэнергии?</h2>
<p>
	 Щиты учета электроэнергии имеют непростую конструкцию, состоящую из целого ряда деталей:
</p>
<ul>
	<li>Корпус с открывающейся наружу дверцей - последняя оснащена петлями;</li>
	<li>Двух- и трехфазные приборы учета с выводом подключений (разновидность вывода зависит от модели оборудования);</li>
	<li>Определенное число DIN-реек;</li>
	<li>Автоматические переключатели;</li>
	<li>Точка заземления, соединяющаяся с главной сетью объекта;</li>
	<li>Небольшое окошко, позволяющее снимать показания приборов без открытия дверцы;</li>
	<li>Шины, автоматы и прочая модульная аппаратура.</li>
</ul>
<p>
	 Для изготовления корпуса шкафа учета используют полиэстер с высокой стойкостью к возгоранию и механическим повреждениям или оцинкованная сталь толщиной 2-3 мм.
</p>
<p>
	 Комплектация приборов учета предполагает разные типы вывода. Так, подключение может осуществляться как внизу, так и в верхней части шкафа. В случае необходимости оборудование снимается, пломбируется и заменяется новым.
</p>
<h2>Способы крепления</h2>
<p>
	 Шкафы учета электроэнергии могут крепиться следующими способами:
</p>
<ul>
	<li>Столбовое или настенное – считается основным. В этом случае к задней части щита крепят 4 пластины либо же используют монтажную ленту;</li>
	<li>Усиленный монтаж (столовой) – все крепежи выполняют из армированного стекловолокна;</li>
	<li>Усиленное дистанционное крепление (столбовое) – крепежные элементы изготовлены из ударопрочного полиэстера и имеют форму кронштейна, позволяющего удалить ЩУР от опоры на 150 мм.</li>
</ul>
