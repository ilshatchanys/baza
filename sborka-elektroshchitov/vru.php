<h2>Из чего состоит ВРУ?</h2>
<p>
	 Низковольтное вводно-распределительное устройство состоит из нескольких рабочих панелей и блоков, которые связаны между собой:
</p>
<ul>
	<li>Вводный – именно на этот блок поступает электроэнергия от источников энергоснабжения. Включает в себя проводники внешних сетей, а также защитное и коммутационное оборудование (рубильники, выключатели нагрузки, предохранители, переключатели, дифференциальные и автоматические выключатели, УЗО);</li>
	<li>АВР – автоматическое включение резервного питания. Отслеживает параметры внешней сети и в случае малейших сбоев переключает приборы на дополнительную цепь питания;</li>
	<li>Для учета электроэнергии с помощью счетчиков разного типа. Здесь же находится переходная испытательная коробка, к которой можно подключить трехфазный счетчик и трансформатор;</li>
	<li>Распределительный – в этом блоке монтируются устройства для защиты цепей самых разных видов, а также панели подключения проводов и кабелей с гибкими жилами;</li>
	<li>Автоматизированного управления освещением. На выбор оборудования для запуска или отключения источников света влияет общий уровень освещенности.</li>
</ul>
<p>
	 Элементы ВРУ могут находиться в одном шкафу или устанавливаться раздельно – все зависит от конструкции зданий.
</p>
