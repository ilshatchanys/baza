<h2>Из чего состоит ПР-11?</h2>
<p>
	 Распределительный пункт имеет форму металлического шкафа, внутри которого находятся горизонтальные профили с установленными на них устройствами. Там же согласно типовой схеме размещается вводный аппарат, состоящий из рубильника серии ВР, РЕ, РБ или выключателя категории ВА57-39 либо ВА51-35. Впрочем, выпуск данной продукции допустим без этого оборудования. Кроме этого внутри шкафа находятся шина защиты и нулевая рабочая. Если конструкция изготавливается для систем заземления, в ее комплектацию входит изолированная шина нейтрали. При этом стоит отметить, что размеры щита напрямую зависят от наличия дополнительных устройств. Подключение линии электропитания может осуществляться к вводному выключателю, а отходящие линии – к автоматическим выключателям с проводниками из меди или алюминия.
</p>
<p>
	 Шкафы ПР-11 устанавливают на полу, вешают на стену или встраивают в нишу. Доступ к внутренним устройствам обеспечивает дверца с одной створкой, запираемой на замок. При необходимости корпус распределительного пункта может иметь уровень защиты IP 54. Эта особенность позволяет вводить и выводить линии отходящего и питающего типа в верхней или нижней части щита. Для данных целей применяют специальные сальники и съемные крышки.
</p>
