	<li>Вводные аппараты Legrand – 2 штуки номиналом 250 Ампер;</li>
	<li>Контакторы;</li>
	<li>Шина;</li>
	<li>Реле контроля напряжения;</li>
	<li>Устройства защиты и коммутации – автоматический выключатель, разъединитель с плавкими вставками, выключатель в виде разъединителя.</li>
</ul>
