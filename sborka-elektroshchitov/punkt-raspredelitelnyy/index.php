<h2>Конструкция ПР</h2>
<p>
	 Распределительный пункт представляет собой металлический щит, разделенный на несколько функциональных зон с тем или иным оборудованием. Подключение входящей линии осуществляется напрямую с помощью специальных зажимов или через вводный аппарат. Размеры и конфигурация ПР выбирают исходя из 2 важных факторов – числа устанавливаемых электрических аппаратов и особенностей проектируемой системы.
</p>
<p>
	 Конструкция распределительного пункта включает в себя следующие элементы:
